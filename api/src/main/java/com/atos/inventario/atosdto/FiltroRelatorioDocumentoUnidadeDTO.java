package com.atos.inventario.atosdto;

public class FiltroRelatorioDocumentoUnidadeDTO {

	private String unidade;

	private String predio;

	private String agrupar;

	private String dataRelatorio;

	
	public String getUnidade() {
		return unidade;
	}

	public void setUnidade(String unidade) {
		this.unidade = unidade;
	}

	public String getPredio() {
		return predio;
	}

	public void setPredio(String predio) {
		this.predio = predio;
	}

	public String getAgrupar() {
		return agrupar;
	}

	public void setAgrupar(String agrupar) {
		this.agrupar = agrupar;
	}

	public String getDataRelatorio() {
		return dataRelatorio;
	}

	public void setDataRelatorio(String dataRelatorio) {
		this.dataRelatorio = dataRelatorio;
	}

}
